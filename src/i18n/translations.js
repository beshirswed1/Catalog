export const getTranslation = (lang, path) => {
    const keys = path.split('.');
    let result = translations[lang];
    for (const key of keys) {
        if (result && result[key] !== undefined) {
            result = result[key];
        } else {
            // Fallback to Arabic
            result = translations['ar'];
            for (const k of keys) {
                if (result && result[k] !== undefined) {
                    result = result[k];
                } else {
                    return path;
                }
            }
            return result;
        }
    }
    return result;
};
